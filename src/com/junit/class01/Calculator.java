public class Calculator {

    public int sum (int a, int b){
        return a + b;
    }

    public int sub (int a, int b){
        return a - b;
    }

    public int mul (int a, int b){
        return a * b;
    }

    public static int div (int a, int b){
        return a / b;
    }

    public int mod (int a, int b){
        return a % b;
    }

}
